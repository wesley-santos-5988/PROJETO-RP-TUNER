void atualizar_ecra_afinacao(float freq_lida) {
    oled_clear();

    char buf[32];

    // Linha 1: Navegação das Cordas (Ex: CORDA: 1' 2  3  4  5  6 )
    oled_draw_string(0, 0, "CORDA:");
    
    int pos_x = 42; // Posição X onde os números começam
    for (int i = 0; i < 6; i++) {
        // Pega apenas o primeiro caractere (o número) de "1a", "2a", etc.
        char num = violao[i].numero_corda[0]; 
        
        if (i == corda_atual) {
            sprintf(buf, "%c'", num); // Corda selecionada ganha o apóstrofo
        } else {
            sprintf(buf, "%c ", num); // Cordas normais ganham um espaço
        }
        
        oled_draw_string(pos_x, 0, buf);
        pos_x += 14; // Avança o cursor X para o próximo número (cabe certinho nos 128 pixels)
    }

    // Linha 2: Nota Alvo e Frequência
    sprintf(buf, "ALVO: %s (%.1fHz)", violao[corda_atual].nome_nota, violao[corda_atual].freq_alvo);
    oled_draw_string(0, 20, buf);

    // Linha 3: Separador visual
    oled_draw_string(0, 35, "---------------------");

    // Linha 4: Frequência lida em tempo real
    sprintf(buf, "LIDO: %.1f Hz", freq_lida);
    oled_draw_string(0, 50, buf);

    oled_update(); // Manda desenhar na vida real!
}