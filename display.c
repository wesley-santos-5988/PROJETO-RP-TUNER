// Envia um comando para o OLED
static void oled_send_cmd(uint8_t cmd) {
    uint8_t buf[2] = {0x00, cmd};
    i2c_write_blocking(i2c1, OLED_ADDR, buf, 2, false);
}

// Inicializa o ecrã
void oled_init(void) {
    uint8_t cmds[] = {
        0xAE, 0xD5, 0x80, 0xA8, 0x3F, 0xD3, 0x00, 0x40, 
        0x8D, 0x14, 0x20, 0x00, 0xA1, 0xC8, 0xDA, 0x12, 
        0x81, 0xCF, 0xD9, 0xF1, 0xDB, 0x40, 0xA4, 0xA6, 0xAF
    };
    for(int i = 0; i < sizeof(cmds); i++) {
        oled_send_cmd(cmds[i]);
    }
    oled_clear();
    oled_update();
}

// Limpa o buffer
void oled_clear(void) {
    memset(oled_buffer, 0, sizeof(oled_buffer));
}

// Atualiza o ecrã físico com os dados do buffer
void oled_update(void) {
    uint8_t payload[OLED_WIDTH + 1];
    payload[0] = 0x40; // Co=0, D/C=1 (Data)

    for (uint8_t page = 0; page < 8; page++) {
        oled_send_cmd(0xB0 + page);
        oled_send_cmd(0x00);
        oled_send_cmd(0x10);
        memcpy(&payload[1], &oled_buffer[page * OLED_WIDTH], OLED_WIDTH);
        i2c_write_blocking(i2c1, OLED_ADDR, payload, OLED_WIDTH + 1, false);
    }
}

// Desenha um caractere
void oled_draw_char(uint8_t x, uint8_t y, char c) {
    if (x >= OLED_WIDTH || y >= OLED_HEIGHT || c < 32 || c > 126) return;
    uint16_t idx = (c - 32) * 5;
    for (uint8_t i = 0; i < 5; i++) {
        uint8_t line = font[idx + i];
        for (uint8_t j = 0; j < 8; j++) {
            if (line & (1 << j)) {
                oled_buffer[((y + j) / 8) * OLED_WIDTH + (x + i)] |= (1 << ((y + j) % 8));
            }
        }
    }
}

// Desenha um texto inteiro
void oled_draw_string(uint8_t x, uint8_t y, const char *str) {
    while (*str) {
        oled_draw_char(x, y, *str++);
        x += 6; // Espaçamento entre letras
        if (x >= OLED_WIDTH) { x = 0; y += 8; }
    }
}

